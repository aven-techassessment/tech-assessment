package com.haven.techassessment.model.account;

import com.haven.techassessment.model.Result;
import com.haven.techassessment.model.TMDbObject;

import java.util.List;

public class AccountWatchList implements TMDbObject {

    private int page;
    private List<Result> results = null;
    private int total_pages;
    private int total_results;

    public int getPage() {
        return page;
    }

    public void setPage(int page) {
        this.page = page;
    }

    public List<Result> getResults() {
        return results;
    }

    public void setResults(List<Result> results) {
        this.results = results;
    }

    public int getTotalPages() {
        return total_pages;
    }

    public void setTotalPages(int total_pages) {
        this.total_pages = total_pages;
    }

    public int getTotalResults() {
        return total_results;
    }

    public void setTotalResults(int total_results) {
        this.total_results = total_results;
    }

}